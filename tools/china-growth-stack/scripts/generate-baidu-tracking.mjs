#!/usr/bin/env node

/**
 * Generate Baidu Analytics (百度统计) tracking code.
 * Outputs HTML snippet + JS helper module with AARRR events pre-defined.
 *
 * Usage: node scripts/generate-baidu-tracking.mjs --site-id YOUR_SITE_ID [--output output/]
 */

import { mkdirSync, writeFileSync } from 'node:fs';
import { resolve, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const currentDir = dirname(fileURLToPath(import.meta.url));

function parseArgs() {
  const args = process.argv.slice(2);
  const options = { siteId: null, output: resolve(currentDir, '../output') };
  for (let i = 0; i < args.length; i++) {
    if (args[i] === '--site-id' && args[i + 1]) options.siteId = args[++i];
    else if (args[i] === '--output' && args[i + 1]) options.output = resolve(args[++i]);
  }
  return options;
}

const EVENTS = [
  { category: 'user', action: 'sign_up', labelParam: 'method', description: 'Registration completed' },
  { category: 'user', action: 'onboarding_complete', labelParam: 'steps', description: 'Onboarding flow completed' },
  { category: 'product', action: 'first_value_delivered', labelParam: 'feature_name', description: 'First core value delivered', hasValue: true },
  { category: 'product', action: 'feature_used', labelParam: 'feature_name', description: 'Core feature used', hasValue: true },
  { category: 'product', action: 'content_generated', labelParam: 'content_type', description: 'Content generated' },
  { category: 'social', action: 'share', labelParam: 'method', description: 'Shared' },
  { category: 'feedback', action: 'submitted', labelParam: 'rating', description: 'Feedback submitted' },
  { category: 'payment', action: 'pricing_view', labelParam: 'source_page', description: 'Pricing page viewed' },
  { category: 'payment', action: 'checkout_start', labelParam: 'plan_name', description: 'Checkout started', hasValue: true },
  { category: 'payment', action: 'purchase', labelParam: 'plan_name', description: 'Purchase completed', hasValue: true },
  { category: 'payment', action: 'renewal', labelParam: 'plan_name', description: 'Subscription renewed', hasValue: true },
  { category: 'payment', action: 'refund', labelParam: 'reason', description: 'Refunded' },
  { category: 'landing', action: 'view', labelParam: 'page_variant', description: 'Landing page viewed' },
];

function generateHtmlSnippet(siteId) {
  return `<!-- Baidu Analytics (百度统计) - Paste into <head> -->
<script>
var _hmt = _hmt || [];
(function() {
  var hm = document.createElement("script");
  hm.src = "https://hm.baidu.com/hm.js?${siteId}";
  var s = document.getElementsByTagName("script")[0];
  s.parentNode.insertBefore(hm, s);
})();
</script>`;
}

function toCamelCase(str) {
  return str.replace(/_([a-z])/g, (_, letter) => letter.toUpperCase());
}

function generateHelperModule(siteId) {
  const functions = EVENTS.map((event) => {
    const funcName = `track${capitalize(toCamelCase(event.action))}`;
    const valueParam = event.hasValue ? ', value' : '';
    const valueArg = event.hasValue ? ', value' : '';

    return `/**
 * ${event.description}
 */
export function ${funcName}(${event.labelParam}${valueParam}) {
  _hmt.push(['_trackEvent', '${event.category}', '${event.action}', ${event.labelParam}${valueArg}]);
}`;
  }).join('\n\n');

  return `/**
 * Baidu Analytics (百度统计) Tracking Helpers
 * Site ID: ${siteId}
 * Generated by startup-playbook china-growth-stack
 *
 * After loading Baidu Analytics code in page <head>, import this module to call tracking functions.
 */

/* global _hmt */

${functions}
`;
}

function capitalize(str) {
  return str.charAt(0).toUpperCase() + str.slice(1);
}

function main() {
  const options = parseArgs();
  if (!options.siteId) {
    console.error('Usage: node scripts/generate-baidu-tracking.mjs --site-id YOUR_BAIDU_SITE_ID [--output output/]');
    process.exit(1);
  }

  mkdirSync(options.output, { recursive: true });

  const htmlPath = resolve(options.output, 'baidu-snippet.html');
  const jsPath = resolve(options.output, 'baidu-tracking.mjs');

  writeFileSync(htmlPath, generateHtmlSnippet(options.siteId), 'utf-8');
  writeFileSync(jsPath, generateHelperModule(options.siteId), 'utf-8');

  console.log(`\n✅ Baidu Analytics (百度统计) tracking code generated:\n`);
  console.log(`   HTML snippet: ${htmlPath}`);
  console.log(`   JS helpers:   ${jsPath}`);
  console.log(`\n   Site ID: ${options.siteId}`);
  console.log(`   Events: ${EVENTS.length}`);
  console.log(`\n📋 Next steps:`);
  console.log(`   1. Paste the HTML snippet into page <head>`);
  console.log(`   2. Import baidu-tracking.mjs and call tracking functions`);
  console.log(`   3. Confirm data in Baidu Analytics backend → Management → Event Analysis\n`);
}

main();
