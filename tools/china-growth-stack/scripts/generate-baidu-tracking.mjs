#!/usr/bin/env node

/**
 * Generate Baidu Analytics (百度统计) tracking code.
 * Outputs HTML snippet + JS helper module with AARRR events pre-defined.
 *
 * Usage: node scripts/generate-baidu-tracking.mjs --site-id YOUR_SITE_ID [--output output/]
 */

import { mkdirSync, writeFileSync } from 'node:fs';
import { resolve, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const currentDir = dirname(fileURLToPath(import.meta.url));

function parseArgs() {
  const args = process.argv.slice(2);
  const options = { siteId: null, output: resolve(currentDir, '../output') };
  for (let i = 0; i < args.length; i++) {
    if (args[i] === '--site-id' && args[i + 1]) options.siteId = args[++i];
    else if (args[i] === '--output' && args[i + 1]) options.output = resolve(args[++i]);
  }
  return options;
}

const EVENTS = [
  { category: 'user', action: 'sign_up', labelParam: 'method', description: '注册完成' },
  { category: 'user', action: 'onboarding_complete', labelParam: 'steps', description: '引导流程完成' },
  { category: 'product', action: 'first_value_delivered', labelParam: 'feature_name', description: '首次获得核心价值', hasValue: true },
  { category: 'product', action: 'feature_used', labelParam: 'feature_name', description: '使用核心功能', hasValue: true },
  { category: 'product', action: 'content_generated', labelParam: 'content_type', description: '生成内容' },
  { category: 'social', action: 'share', labelParam: 'method', description: '分享' },
  { category: 'feedback', action: 'submitted', labelParam: 'rating', description: '提交反馈' },
  { category: 'payment', action: 'pricing_view', labelParam: 'source_page', description: '查看定价页' },
  { category: 'payment', action: 'checkout_start', labelParam: 'plan_name', description: '开始支付', hasValue: true },
  { category: 'payment', action: 'purchase', labelParam: 'plan_name', description: '支付成功', hasValue: true },
  { category: 'payment', action: 'renewal', labelParam: 'plan_name', description: '续费', hasValue: true },
  { category: 'payment', action: 'refund', labelParam: 'reason', description: '退款' },
  { category: 'landing', action: 'view', labelParam: 'page_variant', description: '落地页浏览' },
];

function generateHtmlSnippet(siteId) {
  return `<!-- 百度统计 - 粘贴到 <head> 中 -->
<script>
var _hmt = _hmt || [];
(function() {
  var hm = document.createElement("script");
  hm.src = "https://hm.baidu.com/hm.js?${siteId}";
  var s = document.getElementsByTagName("script")[0];
  s.parentNode.insertBefore(hm, s);
})();
</script>`;
}

function toCamelCase(str) {
  return str.replace(/_([a-z])/g, (_, letter) => letter.toUpperCase());
}

function generateHelperModule(siteId) {
  const functions = EVENTS.map((event) => {
    const funcName = `track${capitalize(toCamelCase(event.action))}`;
    const valueParam = event.hasValue ? ', value' : '';
    const valueArg = event.hasValue ? ', value' : '';

    return `/**
 * ${event.description}
 */
export function ${funcName}(${event.labelParam}${valueParam}) {
  _hmt.push(['_trackEvent', '${event.category}', '${event.action}', ${event.labelParam}${valueArg}]);
}`;
  }).join('\n\n');

  return `/**
 * 百度统计 Tracking Helpers
 * Site ID: ${siteId}
 * Generated by startup-playbook china-growth-stack
 *
 * 在页面 <head> 中加载百度统计代码后，导入此模块调用追踪函数。
 */

/* global _hmt */

${functions}
`;
}

function capitalize(str) {
  return str.charAt(0).toUpperCase() + str.slice(1);
}

function main() {
  const options = parseArgs();
  if (!options.siteId) {
    console.error('Usage: node scripts/generate-baidu-tracking.mjs --site-id YOUR_BAIDU_SITE_ID [--output output/]');
    process.exit(1);
  }

  mkdirSync(options.output, { recursive: true });

  const htmlPath = resolve(options.output, 'baidu-snippet.html');
  const jsPath = resolve(options.output, 'baidu-tracking.mjs');

  writeFileSync(htmlPath, generateHtmlSnippet(options.siteId), 'utf-8');
  writeFileSync(jsPath, generateHelperModule(options.siteId), 'utf-8');

  console.log(`\n✅ 百度统计追踪代码已生成:\n`);
  console.log(`   HTML snippet: ${htmlPath}`);
  console.log(`   JS helpers:   ${jsPath}`);
  console.log(`\n   Site ID: ${options.siteId}`);
  console.log(`   Events: ${EVENTS.length}`);
  console.log(`\n📋 下一步:`);
  console.log(`   1. 将 HTML snippet 粘贴到页面 <head> 中`);
  console.log(`   2. 导入 baidu-tracking.mjs 并调用追踪函数`);
  console.log(`   3. 在百度统计后台 → 管理 → 事件分析 中确认数据\n`);
}

main();
